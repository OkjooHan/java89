package step04;

public class Exam036_6 {
  public static void main(String[] args) {
    //1) String --> int
    String s1 = "1234";
    int v1 = Integer.parseInt(s1);
    System.out.println(v1);

    //2) String --> Float
    String s2 = "3.14";
    float v2 = Float.parseFloat(s2);
    System.out.println(v2);
  }
}
