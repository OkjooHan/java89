package step04;

public class Exam039_0 {
  public static void main(String[] args) {
    boolean v = 10 == 20;
    System.out.println(v);

    v = 10 != 20;
    System.out.println(v);

    v = 10 < 20;
    System.out.println(v);

    v = 10 <= 20;
    System.out.println(v);

    v = 10 > 20;
    System.out.println(v);

    v = 10 >= 20;
    System.out.println(v);

    System.out.println(3.14f > 3.12f);
    System.out.println(3.14 > 3.1400000001);
    System.out.println('가' < '각');
  }
}
